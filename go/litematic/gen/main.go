// gen 把 blocks_1_12.json + zh_cn.json 预计算为 Go map 字面量，
// 消除 embed 776 KB JSON 和运行时 json.Unmarshal 的开销。
//
// 用法: go run ./gen (在 go/litematic 目录下 go generate)
package main

import (
	"encoding/json"
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strings"
)

type prBlock struct {
	ID         int    `json:"id"`
	Name       string `json:"name"`
	Variations []struct {
		Metadata    int    `json:"metadata"`
		DisplayName string `json:"displayName"`
	} `json:"variations"`
}

func main() {
	dir := filepath.Dir(".")
	blocksPath := filepath.Join(dir, "blocks_1_12.json")
	zhPath := filepath.Join(dir, "zh_cn.json")
	outPath := filepath.Join(dir, "block_ids_data.go")

	// 1. 解析 blocks_1_12.json
	blocksData, err := os.ReadFile(blocksPath)
	if err != nil {
		fmt.Fprintf(os.Stderr, "读取 %s 失败: %v\n", blocksPath, err)
		os.Exit(1)
	}
	var blocks []prBlock
	if err := json.Unmarshal(blocksData, &blocks); err != nil {
		fmt.Fprintf(os.Stderr, "解析 %s 失败: %v\n", blocksPath, err)
		os.Exit(1)
	}

	// 2. 解析 zh_cn.json
	zhData, err := os.ReadFile(zhPath)
	if err != nil {
		fmt.Fprintf(os.Stderr, "读取 %s 失败: %v\n", zhPath, err)
		os.Exit(1)
	}
	var zhRaw map[string]string
	if err := json.Unmarshal(zhData, &zhRaw); err != nil {
		fmt.Fprintf(os.Stderr, "解析 %s 失败: %v\n", zhPath, err)
		os.Exit(1)
	}

	// 3. 生成 Go 代码
	var sb strings.Builder
	sb.WriteString("// Code generated by go generate; DO NOT EDIT.\n")
	sb.WriteString("package litematic\n\n")

	// blockIDToName
	sb.WriteString("var blockIDToName = map[int]string{\n")
	for _, b := range blocks {
		sb.WriteString(fmt.Sprintf("\t%d: %q,\n", b.ID, "minecraft:"+b.Name))
	}
	sb.WriteString("}\n\n")

	// blockVariantNames (含 metadata 变体)
	sb.WriteString("var blockVariantNames = map[string]string{\n")
	// 为了输出稳定，先收集再排序
	type variantEntry struct{ key, val string }
	var variants []variantEntry
	for _, b := range blocks {
		if len(b.Variations) == 0 {
			variants = append(variants, variantEntry{
				key: fmt.Sprintf("%d:0", b.ID),
				val: "minecraft:" + b.Name,
			})
			continue
		}
		for _, v := range b.Variations {
			key := fmt.Sprintf("%d:%d", b.ID, v.Metadata)
			val := "minecraft:" + b.Name
			if v.DisplayName != "" {
				val = "minecraft:" + toName(v.DisplayName)
			}
			variants = append(variants, variantEntry{key, val})
		}
	}
	sort.Slice(variants, func(i, j int) bool { return variants[i].key < variants[j].key })
	for _, ve := range variants {
		sb.WriteString(fmt.Sprintf("\t%q: %q,\n", ve.key, ve.val))
	}
	sb.WriteString("}\n\n")

	// blockNameToZH (仅 block.minecraft.*)
	sb.WriteString("var blockNameToZH = map[string]string{\n")
	prefix := "block.minecraft."
	var zhEntries []struct{ key, val string }
	for k, v := range zhRaw {
		if strings.HasPrefix(k, prefix) {
			zhEntries = append(zhEntries, struct{ key, val string }{
				key: strings.TrimPrefix(k, prefix),
				val: v,
			})
		}
	}
	sort.Slice(zhEntries, func(i, j int) bool { return zhEntries[i].key < zhEntries[j].key })
	for _, e := range zhEntries {
		sb.WriteString(fmt.Sprintf("\t%q: %q,\n", e.key, e.val))
	}
	sb.WriteString("}\n")

	// 4. 写入输出文件
	if err := os.WriteFile(outPath, []byte(sb.String()), 0644); err != nil {
		fmt.Fprintf(os.Stderr, "写入 %s 失败: %v\n", outPath, err)
		os.Exit(1)
	}
	fmt.Printf("生成完成: %s (%d block ID, %d variant, %d zh_cn 条目)\n",
		outPath, len(blocks), len(variants), len(zhEntries))
}

func toName(display string) string {
	return strings.ToLower(strings.ReplaceAll(display, " ", "_"))
}
